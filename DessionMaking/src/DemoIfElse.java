public class DemoIfElse {
    public static void main(String[] args) {
        int x = 5, y =10;
        if (x > y) {
            System.out.println("Giá trị của x lớn hơn y");
        }
        else if (x == y ){
            System.out.println("Giá trị của x bằng y");
        }
        else {
            System.out.println("Giá trị của x nhỏ hơn y");
        }
    }
}
