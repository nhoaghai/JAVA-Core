public class DemoSwitchCase {
    public static void main(String[] args) {
        int x = 5;
        switch (x){
            case 1:
                System.out.println("giá trị của x = 1");
                break;
            case 2:
                System.out.println("giá trị của x = 2");
                break;
            case 3:
                System.out.println("giá trị của x = 3");
                break;
            default:
                System.out.println("Giá trị cảu x lơn hơn 3");
        }
    }
}
